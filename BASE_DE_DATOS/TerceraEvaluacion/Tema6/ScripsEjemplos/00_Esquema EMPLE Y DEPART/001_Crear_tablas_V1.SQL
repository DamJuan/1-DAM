--CREACIÓN ESQUEMA TRADUCIDO SCOTT (EMPLE Y DEPART)
--TODAS LAS RESTRICCIONES A NIVEL DE TABLA

CREATE TABLE DEPART (
DEPT_NO    NUMBER(2),
DNOMBRE    VARCHAR2(14), 
LOC        VARCHAR2(14),
CONSTRAINT DEPART_PK PRIMARY KEY (DEPT_NO)
) 
TABLESPACE USERS;

CREATE TABLE EMPLE (
EMP_NO     NUMBER(4),
APELLIDO   VARCHAR2(10),
OFICIO     VARCHAR2(10),
DIR        NUMBER(4),
FECHA_ALT  DATE,
SALARIO    NUMBER(7,2),
COMISION   NUMBER(7,2),
DEPT_NO    NUMBER(2) 
           CONSTRAINT EMPLE_DEPT_VNN NOT NULL,
CONSTRAINT EMPLE_PK PRIMARY KEY (EMP_NO),    
CONSTRAINT EMPLE_EMPLE_FK FOREIGN KEY (DIR) REFERENCES EMPLE (EMP_NO),
CONSTRAINT EMPLE_DEPART_FK FOREIGN KEY (DEPT_NO) REFERENCES DEPART (DEPT_NO)
) 
TABLESPACE USERS;