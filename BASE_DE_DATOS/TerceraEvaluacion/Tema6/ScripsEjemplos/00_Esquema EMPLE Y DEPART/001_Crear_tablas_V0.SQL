--CREACIÓN ESQUEMA TRADUCIDO SCOTT (EMPLE Y DEPART)
--RESTRICCIONES A NIVEL DE COLUMNA (LAS QUE SE PUEDAN)
--SOLUCIÓN ACADEMICA:
    --SE CREA LA CLAVE AJENA REFLEXIVA DESDE EL PRINCIPIO
    --LO MÁS LÓGICO SI SE VAN A INTRODUCIR TODOS LOS DATOS DESDE CERO VIA APLICACIÓN
    --SI NO ES ASÍ HAY QUE TENERLO EN CUENTA AL CARGAR DATOS POSTERIORMENTE

CREATE TABLE DEPART (
DEPT_NO     NUMBER(2) 
            CONSTRAINT DEPART_PK PRIMARY KEY,
DNOMBRE     VARCHAR2(14), 
LOC         VARCHAR2(14)
) 
TABLESPACE USERS;

CREATE TABLE EMPLE (
EMP_NO      NUMBER(4) 
            CONSTRAINT EMPLE_PK PRIMARY KEY,
APELLIDO    VARCHAR2(10),
OFICIO      VARCHAR2(10),
DIR         NUMBER(4)
            CONSTRAINT EMPLE_EMPLE_FK REFERENCES EMPLE (EMP_NO),
FECHA_ALT   DATE,
SALARIO     NUMBER(7,2),
COMISION    NUMBER(7,2),
DEPT_NO     NUMBER(2) 
            CONSTRAINT EMPLE_DEPT_VNN NOT NULL
            CONSTRAINT EMPLE_DEPART_FK REFERENCES DEPART (DEPT_NO)
) 
TABLESPACE USERS;