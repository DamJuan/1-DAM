--CREACIÓN ESQUEMA TRADUCIDO SCOTT (EMPLE Y DEPART)
--NO SE CREA LA CLAVE AJENA REFLEXIVA PARA HACER MÁS CÓMODA LA CARGA POSTERIOR DE DATOS
    --SE MODIFICARÁ POSTERIORMENTE LA TABLA AÑADIENDO LA RESTRICIÓN
    --SOLO DEBE HACERSE ESTO SI SE VA A PRODUCIR UNA CARGA POSTERIOR DE DATOS Y ESTAMOS SEGUROS DE QUE LOS DATOS A CARGAR SON CORRECTOS

CREATE TABLE DEPART (
DEPT_NO     NUMBER(2) 
            CONSTRAINT DEPART_PK PRIMARY KEY,
DNOMBRE     VARCHAR2(14), 
LOC         VARCHAR2(14)
) 
TABLESPACE USERS;

CREATE TABLE EMPLE (
EMP_NO      NUMBER(4) 
            CONSTRAINT EMPLE_PK PRIMARY KEY,
APELLIDO    VARCHAR2(10),
OFICIO      VARCHAR2(10),
DIR         NUMBER(4),
FECHA_ALT   DATE,
SALARIO     NUMBER(7,2),
COMISION    NUMBER(7,2),
DEPT_NO     NUMBER(2) 
            CONSTRAINT EMPLE_DEPT_VNN NOT NULL
            CONSTRAINT EMPLE_DEPART_FK REFERENCES DEPART (DEPT_NO)
) 
TABLESPACE USERS;