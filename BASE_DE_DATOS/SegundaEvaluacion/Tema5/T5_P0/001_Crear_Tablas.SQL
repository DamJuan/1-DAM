-- TABLA GASTOS
CREATE TABLE GASTOS (
  ID NUMBER CONSTRAINT GASTOS_PK PRIMARY KEY,
  FECHA DATE,
  CONCEPTO VARCHAR2(25),
  CANT NUMBER(3),
  PRECIOUD NUMBER(5,2)
);

-- TABLA LIBRERIA
CREATE TABLE LIBRERIA (
  TEMA CHAR(15) CONSTRAINT LIBRERIA_PK PRIMARY KEY,
  ESTANTE CHAR(2),
  EJEMPLARES NUMBER(2)
) 
TABLESPACE  USERS;

--TABLA NOMBRES
CREATE TABLE NOMBRES
(
  NOMBRE VARCHAR2(15),
  EDAD NUMBER(2)
)
TABLESPACE USERS;

-- TABLA MISTEXTOS
CREATE TABLE MISTEXTOS (
 TITULO  VARCHAR2(32) CONSTRAINT MISTEXTOS_PK PRIMARY KEY,
 AUTOR   VARCHAR2(22),
 EDITORIAL VARCHAR2(15), 
 PAGINA  NUMBER(3)
) 
TABLESPACE USERS;

-- TABLA LIBROS
CREATE TABLE LIBROS (
 TITULO  VARCHAR2(32) CONSTRAINT LIBROS_PK PRIMARY KEY,
 AUTOR   VARCHAR2(22),
 EDITORIAL VARCHAR2(15), 
 PAGINA  NUMBER(3)
) 
TABLESPACE USERS;

-- TABLA NACIMIENTOS
CREATE TABLE NACIMIENTOS (
 NOMBRE CHAR(15) CONSTRAINT NACIMIENTOS_PK PRIMARY KEY,
 APELLIDO CHAR(15),
 FECHANAC DATE,
 EDAD NUMBER
) 
TABLESPACE USERS;


