--TRUNCADO/REDONDEO A DÍAS 'DD'
--LAS HORAS SON IMPORTANTES 

ALTER SESSION SET NLS_DATE_FORMAT='dd/mm/yy hh24:mi';

SELECT  TRUNC (TO_DATE('10/03/19 11:30'), 'dd'), ROUND(TO_DATE('10/03/19 11:30'), 'dd')
FROM    DUAL;

SELECT  TRUNC (TO_DATE('10/03/19 20:30'), 'dd'), ROUND(TO_DATE('10/03/19 20:30'), 'dd')
FROM    DUAL;

SELECT  TRUNC (SYSDATE, 'dd'), ROUND(SYSDATE, 'dd')
FROM    DUAL;